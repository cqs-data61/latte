/* Cooley-Tukey FFT */
void fft(POLY_FFT *a, const uint64_t n)
{
	uint64_t t = n;
	uint64_t m, i, j;
	uint64_t j1, j2;
	mpc_t x, u;
	
	fft_init();
		
	mpc_init2(x, PREC);
	mpc_init2(u, PREC);
	
	for (m = 1; m < n; m <<= 1)
	{
		t >>= 1;
		for (i = 0; i < m; i++)
		{
			j1 = (i * t) << 1;
			j2 = j1 + t - 1;
			
			for (j = j1; j <= j2; j++)
			{
				mpc_set(x, a->poly[j], MPC_RNDNN);		
				mpc_mul(u, a->poly[j + t], fft_zeta[m + i], MPC_RNDNN);
				mpc_add(a->poly[j], x, u, MPC_RNDNN);
				mpc_sub(a->poly[j + t], x, u, MPC_RNDNN);
			}
		}
	}

	mpc_clear(x);
	mpc_clear(u);
}

/* Gentleman-Sande IFFT */
void ifft(POLY_FFT *a, const uint64_t n)
{
	uint64_t t = 1;
	uint64_t m, i, j;
	uint64_t j1, j2;
	uint64_t h = n;
	mpc_t x, y;
	
	fft_init();
	
	mpc_init2(x, PREC);
	mpc_init2(y, PREC);
	
	for (m = n; m > 1; m >>= 1)
	{
		j1 = 0;
		h >>= 1;

		for (i = 0; i < h; i++)
		{
			j2 = j1 + t - 1;
			
			for (j = j1; j <= j2; j++)
			{
				mpc_add(x, a->poly[j], a->poly[j + t], MPC_RNDNN);
				mpc_sub(y, a->poly[j], a->poly[j + t], MPC_RNDNN);		
				mpc_set(a->poly[j], x, MPC_RNDNN);
				mpc_mul(a->poly[j + t], y, ifft_zeta[h + i], MPC_RNDNN);
			}
			j1 += t << 1;
		}
		t <<= 1;
	}

	for (j = 0; j < n; j++)
	{
		mpc_div_ui(a->poly[j], a->poly[j], n, MPC_RNDNN);
	}

	mpc_clear(x);
	mpc_clear(y);
}

/* splitfft from Falcon */
void split_fft(POLY_FFT *f0, POLY_FFT *f1, const POLY_FFT *a, const uint64_t n)
{
	uint64_t i, j;
	
	fft_init();
	
	mpc_set_fr(f0->poly[0], mpc_realref(a->poly[0]), MPC_RNDNN);
	mpc_set_fr(f1->poly[0], mpc_imagref(a->poly[0]), MPC_RNDNN);
	
	for (i = 0; i < (n >> 1); i++)
	{
		j = i << 1;
		
		mpc_add(f0->poly[i], a->poly[j], a->poly[j + 1], MPC_RNDNN);
		mpc_div_2ui(f0->poly[i], f0->poly[i], 1, MPC_RNDNN);
		mpc_sub(f1->poly[i], a->poly[j], a->poly[j + 1], MPC_RNDNN);
		mpc_mul(f1->poly[i], f1->poly[i], ifft_zeta[(n >> 1) + i], MPC_RNDNN);
		mpc_div_2ui(f1->poly[i], f1->poly[i], 1, MPC_RNDNN);
	}
}

/* splitfft when input is real */
void split_fft_r(POLY_R *f0, POLY_FFT *f1, const POLY_R *a, const uint64_t n)
{
	uint64_t i, j;
	mpfr_t tmp;
	
	fft_init();
	
	mpfr_set(f0->poly[0], a->poly[0], MPFR_RNDN);
	mpc_set_ui(f1->poly[0], 0, MPC_RNDNN);
	
	mpfr_init2(tmp, PREC);
	for (i = 0; i < (n >> 1); i++)
	{
		j = i << 1;
		
		mpfr_add(f0->poly[i], a->poly[j], a->poly[j + 1], MPFR_RNDN);
		mpfr_div_2ui(f0->poly[i], f0->poly[i], 1, MPFR_RNDN);
		mpfr_sub(tmp, a->poly[j], a->poly[j + 1], MPFR_RNDN);
		mpfr_div_2ui(tmp, tmp, 1, MPFR_RNDN);
		mpc_mul_fr(f1->poly[i], ifft_zeta[(n >> 1) + i], tmp, MPC_RNDNN);
	}
	mpfr_clear(tmp);
}

/* mergefft from Falcon */
void merge_fft(POLY_FFT *a, const POLY_FFT *f0, const POLY_FFT *f1, const uint64_t n)
{
	uint64_t i, j;
	
	fft_init();
	
	mpc_set_fr_fr(a->poly[0], mpc_realref(f0->poly[0]), mpc_realref(f1->poly[0]), MPC_RNDNN);
	
	for (i = 0; i < (n >> 1); i++)
	{
		j = i << 1;
		
		mpc_mul(a->poly[j + 1], f1->poly[i], fft_zeta[(n >> 1) + i], MPC_RNDNN);
		mpc_add(a->poly[j], f0->poly[i], a->poly[j + 1], MPC_RNDNN);
		mpc_sub(a->poly[j + 1], f0->poly[i], a->poly[j + 1], MPC_RNDNN);
	}
}
