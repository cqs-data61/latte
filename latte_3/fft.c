/* Cooley-Tukey FFT */
void fft(POLY_FFT *a, const uint64_t n)
{
	uint64_t t = n;
	uint64_t m, i, j;
	uint64_t j1, j2;
	mpc_t x, u;
	
	fft_init();
		
	mpc_init2(x, PREC);
	mpc_init2(u, PREC);
	
	for (m = 1; m < n; m <<= 1)
	{
		t >>= 1;
		for (i = 0; i < m; i++)
		{
			j1 = (i * t) << 1;
			j2 = j1 + t - 1;
			
			for (j = j1; j <= j2; j++)
			{
				mpc_set(x, a->poly[j], MPC_RNDNN);		
				mpc_mul(u, a->poly[j + t], fft_zeta[m + i], MPC_RNDNN);
				mpc_add(a->poly[j], x, u, MPC_RNDNN);
				mpc_sub(a->poly[j + t], x, u, MPC_RNDNN);
			}
		}
	}

	mpc_clear(x);
	mpc_clear(u);
}

